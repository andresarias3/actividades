// Paso 1: Crear el arreglo vacío de panes y mostrarlo en consola
let panes = [];
console.log(panes, 'creación de panes');

// Paso 2: Agregar elementos al arreglo usando `push`
panes.push('pan crema', 'palitos', 'churros', 'buñuelos');
console.log(panes, 'agregación de items');

// Paso 3: Eliminar el último elemento del arreglo usando `pop`
panes.pop();
console.log(panes, 'eliminación de último item');
